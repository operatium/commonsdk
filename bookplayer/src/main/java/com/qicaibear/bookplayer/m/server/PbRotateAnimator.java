package com.qicaibear.bookplayer.m.server;


public class PbRotateAnimator   {
    private Integer id;

    private Integer bookId;

    private Integer bookDetailId;

    private String rotateName;

    private Integer rotateTime;

    private Integer delayTime;

    private Integer repeatCount;

    private Integer repeatMode;

    private String point;

    private String propertyName;

    private String rotation;  //度数

    private Integer status;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getBookId() {
        return bookId;
    }

    public void setBookId(Integer bookId) {
        this.bookId = bookId;
    }

    public Integer getBookDetailId() {
        return bookDetailId;
    }

    public void setBookDetailId(Integer bookDetailId) {
        this.bookDetailId = bookDetailId;
    }

    public String getRotateName() {
        return rotateName;
    }

    public void setRotateName(String rotateName) {
        this.rotateName = rotateName;
    }

    public Integer getRotateTime() {
        return rotateTime;
    }

    public void setRotateTime(Integer rotateTime) {
        this.rotateTime = rotateTime;
    }

    public Integer getDelayTime() {
        return delayTime;
    }

    public void setDelayTime(Integer delayTime) {
        this.delayTime = delayTime;
    }

    public Integer getRepeatCount() {
        return repeatCount;
    }

    public void setRepeatCount(Integer repeatCount) {
        this.repeatCount = repeatCount;
    }

    public Integer getRepeatMode() {
        return repeatMode;
    }

    public void setRepeatMode(Integer repeatMode) {
        this.repeatMode = repeatMode;
    }

    public String getPoint() {
        return point;
    }

    public void setPoint(String point) {
        this.point = point;
    }

    public String getPropertyName() {
        return propertyName;
    }

    public void setPropertyName(String propertyName) {
        this.propertyName = propertyName;
    }

    public String getRotation() {
        return rotation;
    }

    public void setRotation(String rotation) {
        this.rotation = rotation;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }
}