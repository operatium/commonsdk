package com.qicaixiong.reader.bookmodel.other;

public class RectAction {

    private String myName;
    private String rectName;
    private String actionGroupName;

    public String getMyName() {
        return myName;
    }

    public void setMyName(String myName) {
        this.myName = myName;
    }

    public String getRectName() {
        return rectName;
    }

    public void setRectName(String rectName) {
        this.rectName = rectName;
    }

    public String getActionGroupName() {
        return actionGroupName;
    }

    public void setActionGroupName(String actionGroupName) {
        this.actionGroupName = actionGroupName;
    }
}
